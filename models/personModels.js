class Person {
  constructor(name, photo, href, username, work, city, from, status, cell, genre, birthday, family, study) {
    this.name = name;
    this.photo = photo;
    this.href = href;
    this.username = username;
    this.work = work;
    this.city = city;
    this.from = from;
    this.status = status;
    this.cell = cell;
    this.genre = genre;
    this.birthday = birthday;
    this.family = family;
    this.study = study;
  }
}

module.exports = Person;